/**
 * <br>HelloTimes10.java
 * <br>This program prints out the word "Hello" ten times
 * <br>November 11, 2019
 *@author Richard Kha
 */
public class HelloTimes10 {
	/**
	 * 
	 * This is the entry point to the program
	 */
	public static void main(String[] args) {

		//loop to print out 10 times
		for (int counter = 0; counter<10; counter++) {
			System.out.println("Hello");
		}

	}

}
